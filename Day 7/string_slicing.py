a="devanshu Kumar"
print(len(a))
print(a[-4:-2])
print(a[len(a)-4:len(a)-2])  #This is the concept that is applied in negative slicing.

print(a[:-1]) #or print(a[:len(a)-1])
print(a[::2]) # it prints every 2nd word of the string.
print(a[::-1]) #it print the string in reverse order.
print(a[4::-1]) # It reverses the string from index 4, all the way to the begining.