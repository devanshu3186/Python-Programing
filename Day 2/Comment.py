# Here we'll learn how to write comments in python.
# Comments are used to explain the code and make it more readable.
# Comments are ignored by the Python interpreter.
# Comments can be used to prevent execution when testing code.
# Comments can be used to make the code more readable.

#The above code is a comment in Python.
#Comments can be written in two ways in Python.
#1. Single line comments, which are written using the hash symbol (#) like this one or above ones.
#2. Multi-line comments, which are written using triple double quotes or triple single quotes.
#example of multi-line comments is given below:
'''
This is a multi-line comment.
And I am writing this comment using triple single quotes.
'''

"""This is also a multi-line comment.
And I am writing this comment using triple double quotes."""

print("Hello World!, Welcome to Python Programming.")
# If you run this code, you will see that the output will be "Hello World!, Welcome to Python Programming."
