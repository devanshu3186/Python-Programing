# In python we have conditional operators, some of them are: 
# 1. == (equal to)
# 2. != (not equal to)
# 3. > (greater than)
# 4. < (less than)
# 5. >= (greater than or equal to)
# 6. <= (less than or equal to)

a=10
b=20
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
